function xy = T2xy(T)

xy = zeros(2, numel(T));
inds = (T > 4000) & (T < 7000);

xy(1, inds) = 0.244063 ... 
		+ 0.09911 * 10 ^ 3 ./ T(inds) ...
		+ 2.9678 * 10 ^ 6 ./ T(inds) .^ 2 ...
		- 4.6070 * 10 ^ 9 ./ T(inds) .^ 3;

xy(1, ~inds) = 0.237040 ... 
		+ 0.24748 * 10 ^ 3 ./ T(~inds) ...
		+ 1.9018 * 10 ^ 6 ./ T(~inds) .^ 2 ...
		- 2.0064 * 10 ^ 9 ./ T(~inds) .^ 3;

xy(2, :) = -3.000 * xy(1, :) .^ 2 + 2.870 * xy(1, :) - 0.275;